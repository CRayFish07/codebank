public class FortuneBean {
    private Integer fortuneId;
    private Integer type;
    private Integer fortuneNumber;
    private String description;
    private String descriptionEn;
    
    private String inputFortune;
    /**
     * @return the fortuneId
     */
    public Integer getFortuneId() {
        return fortuneId;
    }
    /**
     * @param fortuneId the fortuneId to set
     */
    public void setFortuneId(Integer fortuneId) {
        this.fortuneId = fortuneId;
    }
    /**
     * @return the type
     */
    public Integer getType() {
        return type;
    }
    /**
     * @param type the type to set
     */
    public void setType(Integer type) {
        this.type = type;
    }
    /**
     * @return the fortuneNumber
     */
    public Integer getFortuneNumber() {
        return fortuneNumber;
    }
    /**
     * @param fortuneNumber the fortuneNumber to set
     */
    public void setFortuneNumber(Integer fortuneNumber) {
        this.fortuneNumber = fortuneNumber;
    }
    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }
    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }
    /**
     * @return the descriptionEn
     */
    public String getDescriptionEn() {
        return descriptionEn;
    }
    /**
     * @param descriptionEn the descriptionEn to set
     */
    public void setDescriptionEn(String descriptionEn) {
        this.descriptionEn = descriptionEn;
    }
    /**
     * @return the inputFortune
     */
    public String getInputFortune() {
        return inputFortune;
    }
    /**
     * @param inputFortune the inputFortune to set
     */
    public void setInputFortune(String inputFortune) {
        this.inputFortune = inputFortune;
    }
    
    
}
